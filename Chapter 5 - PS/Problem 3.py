# Can we have a set with 18 (int) and '18' (str) as a value in it?

# Yes, we can have a set with 18 (int) and '18' (str) as a value in it. The set is a collection of unique elements. In this case, 18 (int) and '18' (str) are considered as two different elements, so both can be present in the set.

# Let's see an example:

my_set = {18, "18"} # Creating a set with 18 (int) and '18' (str) as a value in it

print(my_set)