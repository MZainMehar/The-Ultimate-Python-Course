# Following are some operators commonly used in Python

# 1. Arithmetic Operators
# Arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication, division, etc.
# +, -, *, /, %, // etc.

# Example
a = 10
b = 20

print(a + b) # answer is 30 # addition

print(a - b) # answer is -10 # subtraction

print(a * b) # answer is 200 # multiplication

print(a / b) # answer is 0.5 # division

print(a % b) # answer is 10 # modulo

print(a // b) # answer is 0 # floor division


# 2. Comparison Operators
# Comparison operators are used to compare two values. It always returns True or False according to the condition. 
# ==, !=, <=, >=, <, > etc.

# Example
a = 10
b = 20

print(a == b) # False

print(a != b) # True

print(a < b) # True

print(a > b) # False

print (a <= b) # True

print(a >= b) # False


# 3. Assignment Operators
# Assignment operators are used to assign values to variables.
# =, +=, -=, *=, /=, %=, //= etc.

# Example
a = 10
b = 20

a += b # This is equal to a = a + b

print(a) # answer is 30

a -= b # This is equal to a = a - b 

print(a) # answer is 10


# 4. Logical Operators
# Logical operators are used to combine conditional statements.
# and, or, not

# Example
a = 10
b = 20

print(a > 5 and b < 30) # True

print(a > 5 or b > 30) # True

print(not(a > 5 and b < 30)) # False

print(not(a == 10)) # False