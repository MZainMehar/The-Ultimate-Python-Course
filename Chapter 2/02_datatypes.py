# Following are some primitive data types in Python

# 1. Integers
a = 5 # class 'int'

# 2. Float
b = 5.0 # class 'float'

# 3. String
c = "Hello" # class 'str'

# 4. Boolean
d = True # class 'bool'

# 5. None
e = None # class 'NoneType'

# Example usage
print(a)
print(b)
print(c)
print(d)
print(e)
