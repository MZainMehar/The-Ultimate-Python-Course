# type() funstion is a built-in function in python that returns the type of the object or given variable.

a = 10
b = "Hello"
c = 10.5

print(type(a)) # <class 'int'>

print(type(b)) # <class 'str'>

print(type(c)) # <class 'float'>


# We can convert the data type of an object to another data type using the built-in functions like int(), float(), str(), etc.

a = 10
b = 20.5
c = "30"

print(float(c)) # 30.0