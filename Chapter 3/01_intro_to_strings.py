# String is a data type in Python that is used to store text.
# Strings are sequences of characters enclosed in quotes.
# Strings can be enclosed in single quotes, double quotes, or triple quotes.

# Single quotes
string1 = 'This is a string enclosed in single quotes.'
print(string1)

# Double quotes
string2 = "This is a string enclosed in double quotes."
print(string2)

# Triple quotes
string3 = '''This is a string enclosed in triple quotes.'''
print(string3)

# Strings are immutable, which means that once a string is created, it cannot be changed.