# Dictionary is a collection of key-value pairs
# Key is unique and value can be anything that means key cannot be duplicated
# Dictionary is mutable
# Dictionary is unordered
# Dictionary is represented by {}
# Dictionary is also called as hash map or associative array

# Creating a dictionary
my_dictionary = {
    "fruit1" : "Apple",
    "fruit2" : "Banana",
    "fruit3" : "Cherry",
    "fruit4" : "Date",
    "fruit5" : "Elderberry",
    "fruit6" : "Fig",
    "list" : [1, 2, 3, 4, 5]
}

print(my_dictionary["fruit5"])

print(my_dictionary["list"])