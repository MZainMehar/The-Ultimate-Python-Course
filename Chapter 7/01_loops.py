# Loops in python are used to execute a block of code multiple times.

# There are two types of loops in python:

# 1. for loop
# 2. while loop

# For loop:
# The for loop in python is used to iterate over a sequence (list, tuple, string) or other iterable objects.

# Example:

# Program to print numbers from 1 to 10

for i in range(1, 11):
    print(i)



# While loop:
# The while loop in python is used to execute a block of statements repeatedly until a given condition is true.
# The diffrenece between the for loop and while loop is that the for loop is used when the number of iterations is known and the while loop is used when the number of iterations is unknown.

# Example:

# Program to print numbers from 1 to 10 using while loop

i = 1
while i<=10:
    print(i)
    i += 1