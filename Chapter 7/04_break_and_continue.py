# Break statement in a loop is used to exit the loop when a certain condition is met.

# Example:

for i in range(100):
    if(i==50):
        break # Exit the loop when i is 50
    print(i)



# Continue statement in a loop is used to skip the current iteration and continue with the next iteration.

# Example:

for i in range(100):
    if(i==50):
        continue # Skip 50 and continue with the next iteration
    print(i)