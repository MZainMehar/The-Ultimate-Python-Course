# For loop with strings
my_string = "Hello"
for i in my_string:
    print(i)


# For loop with list
my_list = ["Ali", "Ahmed", 1, 20.0, "Python"]
for i in my_list:
    print(i)


# For loop with tuple
my_tuple = (1, 2, "Python", 20.0)
for i in my_tuple:
    print(i)    
