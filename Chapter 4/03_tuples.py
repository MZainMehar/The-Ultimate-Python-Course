# A tuple is a collection which is ordered and unchanged. In Python tuples are written with round brackets.

# Tuples are ordered collections of items that can be of different types.

# Tuples are immutable, meaning they cannot be changed in place.

# Tuples are defined by enclosing the elements in parentheses ().

# Elements are separated by commas.

# Tuples can contain any Python object, including other tuples.

# Tuples are faster than lists.

# Tuples are used when the data cannot change, such as the days of the week, or the months of the year.

a = () # Empty tuple
print(a)

a = (1) # Not a tuple
print(a)

a = (1,) # Tuple with only one element needs a comma
print(a)

a = (1, 2, 3, 4, 5)
print(a)